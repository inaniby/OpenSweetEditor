    private bool TryGetSafeCompletionReplacementRange(out TextRange range)
    {
        range = default;
        if (controller.GetWordRangeAtCursor() is not TextRange candidate)
            return false;

        Document? document = controller.GetDocument();
        if (document == null)
            return false;

        TextPosition cursor = controller.GetCursorPosition();
        TextRange normalized = NormalizeRange(candidate);
        if (normalized.Start.Line != cursor.Line || normalized.End.Line != cursor.Line)
            return false;

        string lineText = document.GetLineText(cursor.Line) ?? string.Empty;
        if (normalized.Start.Column < 0 || normalized.Start.Column >= normalized.End.Column || normalized.End.Column > lineText.Length)
            return false;

        int clampedCursor = Math.Clamp(cursor.Column, 0, lineText.Length);
        if (clampedCursor < normalized.Start.Column || clampedCursor > normalized.End.Column)
            return false;

        range = normalized;
        return true;
    }

    private static TextRange NormalizeRange(TextRange range)
    {
        return ComparePositions(range.Start, range.End) <= 0
            ? range
            : new TextRange { Start = range.End, End = range.Start };
    }

    private static int ComparePositions(TextPosition left, TextPosition right)
    {
        int lineCompare = left.Line.CompareTo(right.Line);
        return lineCompare != 0 ? lineCompare : left.Column.CompareTo(right.Column);
    }

    private void MoveCompletionSelection(int delta)
    {
        if (currentCompletionItems.Count == 0)
            return;

        selectedCompletionIndex = Math.Clamp(selectedCompletionIndex + delta, 0, currentCompletionItems.Count - 1);
        RenderCompletionItems();
    }

    private void EnsureSelectedCompletionVisible()
    {
        if (selectedCompletionIndex < 0)
            return;

        double itemTop = selectedCompletionIndex * CompletionRowHeight;
        double itemBottom = itemTop + CompletionRowHeight;
        double viewportTop = completionScrollViewer.Offset.Y;
        double viewportBottom = viewportTop + completionScrollViewer.Viewport.Height;
        if (itemTop < viewportTop)
        {
            completionScrollViewer.Offset = new Vector(0, itemTop);
        }
        else if (itemBottom > viewportBottom)
        {
            completionScrollViewer.Offset = new Vector(0, Math.Max(0, itemBottom - completionScrollViewer.Viewport.Height));
        }
    }

    private bool TryGetSelectedCompletionItem(out CompletionItem? item)
    {
        if (selectedCompletionIndex >= 0 && selectedCompletionIndex < currentCompletionItems.Count)
        {
            item = currentCompletionItems[selectedCompletionIndex];
            return true;
        }

        item = null;
        return false;
    }

