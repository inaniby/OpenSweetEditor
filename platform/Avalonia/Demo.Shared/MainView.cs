    private void RepositionSelectionActionBar()
    {
        if (!selectionActionBar.IsVisible)
        {
            return;
        }

        double hostWidth = editorHost.Bounds.Width;
        double hostHeight = editorHost.Bounds.Height;
        if (hostWidth <= 0 || hostHeight <= 0)
        {
            return;
        }

        double margin = 6;
        double maxWidth = Math.Max(120, hostWidth - margin * 2);
        double minWidth = Math.Min(SelectionActionBarMinWidth, maxWidth);
        selectionActionScrollViewer.MaxWidth = Math.Max(108, maxWidth - 12);
        selectionActionBar.MaxWidth = maxWidth;
        selectionActionBar.Measure(new Size(maxWidth, double.PositiveInfinity));
        Size desiredSize = selectionActionBar.DesiredSize;
        double menuWidth = Math.Clamp(desiredSize.Width, minWidth, maxWidth);
        double menuHeight = Math.Max(36, desiredSize.Height);
        double safeBottom = hostHeight - margin;
        if (DemoPlatformServices.Current?.TryGetImeTopInEditorHostDip(this, editorHost, out double imeTopInHostDip) == true)
        {
            safeBottom = Math.Min(safeBottom, imeTopInHostDip - margin);
        }

        double x;
        double y;
        if (TryGetSelectionActionAnchor(out SelectionActionAnchorSnapshot anchor) && anchor.HasSelection)
        {
            CursorRect start = anchor.StartRect;
            CursorRect end = anchor.EndRect;
            CursorRect cursorRect = anchor.CursorRect.Height > 0 ? anchor.CursorRect : controller.GetCursorRect();
            bool hasSelectionRects = start.Height > 0 && end.Height > 0;
            double lineHeight = Math.Max(1, Math.Max(cursorRect.Height, Math.Max(start.Height, end.Height)));
            double startBottom = start.Y + Math.Max(1, start.Height);
            double endBottom = end.Y + Math.Max(1, end.Height);
            double selectionTop = hasSelectionRects ? Math.Min(start.Y, end.Y) : cursorRect.Y;
            double selectionBottom = hasSelectionRects
                ? Math.Max(startBottom, endBottom)
                : cursorRect.Y + Math.Max(1, cursorRect.Height);
            double anchorXCenter = anchor.HasAnchorPoint
                ? anchor.AnchorPoint.X
                : hasSelectionRects
                    ? (start.X + end.X) * 0.5
                    : cursorRect.X;
            double above;
            double below;
            if (anchor.HasAnchorPoint)
            {
                above = anchor.AnchorPoint.Y - menuHeight - SelectionActionBarVerticalOffset;
                below = anchor.AnchorPoint.Y + lineHeight + SelectionActionBarVerticalOffset;
            }
            else
            {
                above = selectionTop - menuHeight - SelectionActionBarVerticalOffset;
                below = selectionBottom + SelectionActionBarVerticalOffset;
            }
            bool canPlaceAbove = above >= margin;
            bool canPlaceBelow = (below + menuHeight) <= safeBottom;
            x = anchorXCenter - menuWidth * 0.5;
            if (canPlaceAbove)
            {
                y = above;
            }
            else if (canPlaceBelow)
            {
                y = below;
            }
            else
            {
                double spaceAbove = Math.Max(0, selectionTop - margin);
                double spaceBelow = Math.Max(0, safeBottom - selectionBottom);
                y = spaceAbove >= spaceBelow
                    ? Math.Max(margin, Math.Min(above, safeBottom - menuHeight))
                    : Math.Clamp(below, margin, Math.Max(margin, safeBottom - menuHeight));
            }
        }
        else
        {
            CursorRect cursor = anchor.CursorRect.Height > 0 ? anchor.CursorRect : controller.GetCursorRect();
            double anchorX = anchor.HasAnchorPoint ? anchor.AnchorPoint.X : cursor.X;
            double anchorY = anchor.HasAnchorPoint ? anchor.AnchorPoint.Y : cursor.Y;
            double above = anchorY - menuHeight - SelectionActionBarVerticalOffset;
            double below = anchorY + Math.Max(1, cursor.Height) + SelectionActionBarVerticalOffset;
            x = anchorX - menuWidth * 0.5;
            y = above >= margin ? above : below;
        }

        x = Math.Clamp(x, margin, Math.Max(margin, hostWidth - menuWidth - margin));
        y = Math.Clamp(y, margin, Math.Max(margin, safeBottom - menuHeight));
        selectionActionBar.Margin = new Thickness(x, y, 0, 0);
    }

