    private void WireEvents()
    {
        toolbarController.SamplePickerButton.Click += (_, _) => ToggleSamplePickerPopup();
        toolbarController.FileCombo.SelectionChanged += async (_, _) =>
        {
            SyncSamplePickerSelection();
            await sampleLoader.OnSelectedSampleChangedAsync().ConfigureAwait(false);
        };
        toolbarController.UndoButton.Click += (_, _) => ExecuteAction(controller.Undo(), "Undo", "Nothing to undo");
        toolbarController.RedoButton.Click += (_, _) => ExecuteAction(controller.Redo(), "Redo", "Nothing to redo");
        toolbarController.ThemeButton.Click += (_, _) => ToggleTheme();
        toolbarController.WrapButton.Click += (_, _) => CycleWrapMode();
        toolbarController.ZoomOutButton.Click += (_, _) => StepScale(-1);
        toolbarController.ZoomInButton.Click += (_, _) => StepScale(1);
        toolbarController.PerfButton.Click += (_, _) => TogglePerfOverlay();
        toolbarController.HelpButton.Click += (_, _) => ShowKeyboardShortcutsDialog();

        controller.DocumentLoaded += (_, _) =>
        {
            foldCollapsed = false;
            allowPassiveInlineSuggestion = false;
            recentSelectionActionAnchor = null;
            RefreshActionState();
            UpdateStatus(activeSample != null ? $"Loaded: {activeSample.FileName}" : "Document loaded");
            UpdateFeatureStrip();
            ScheduleChromeRefresh(DeferredChromeWork.Summary | DeferredChromeWork.SelectionActionBar);
        };

        controller.TextChanged += (_, e) =>
        {
            allowPassiveInlineSuggestion = true;
            recentSelectionActionAnchor = null;
            CancelPendingInlineSuggestion();
            RefreshActionState();
            ScheduleChromeRefresh(DeferredChromeWork.Summary | DeferredChromeWork.SelectionActionBar);
        };

        controller.CursorChanged += (_, e) =>
        {
            DeferredChromeWork work = DeferredChromeWork.Summary | DeferredChromeWork.CompletionPopup;
            if (IsImeVisibleInEditorHost())
                work |= DeferredChromeWork.CursorVisibility;
            if (selectionActionBar.IsVisible || controller.IsInlineSuggestionShowing() || IsRecentSelectionActionAnchorAvailable())
                work |= DeferredChromeWork.SelectionActionBar;
            ScheduleChromeRefresh(work);
            MaybeShowInlineSuggestion(e.CursorPosition);
        };

        controller.SelectionChanged += (_, e) =>
        {
            if (e.HasSelection && ShouldPreferGestureOnlySelectionAnchor())
                CaptureSelectionActionAnchor(null, e.CursorPosition);
            else
                CaptureSelectionActionAnchor(e.HasSelection ? e.Selection : null, e.CursorPosition);
            CancelPendingInlineSuggestion();
            if (ShouldDeferLargeDocumentUiRefresh())
            {
                Dispatcher.UIThread.Post(() =>
                {
                    DeferredChromeWork work = DeferredChromeWork.Summary | DeferredChromeWork.SelectionActionBar;
                    if (IsImeVisibleInEditorHost())
                        work |= DeferredChromeWork.CursorVisibility;
                    ScheduleChromeRefresh(work);
                    RefreshSelectionActionBarWithStabilization();
                }, DispatcherPriority.Background);
            }
            else
            {
                DeferredChromeWork work = DeferredChromeWork.Summary | DeferredChromeWork.SelectionActionBar;
                if (IsImeVisibleInEditorHost())
                    work |= DeferredChromeWork.CursorVisibility;
                ScheduleChromeRefresh(work);
                RefreshSelectionActionBarWithStabilization();
            }
        };

        controller.ScrollChanged += (_, _) =>
        {
            DeferredChromeWork work = DeferredChromeWork.None;
            if (completionPopup.IsVisible)
                work |= DeferredChromeWork.CompletionPopup;
            if (selectionActionBar.IsVisible || controller.IsInlineSuggestionShowing() || IsRecentSelectionActionAnchorAvailable())
                work |= DeferredChromeWork.SelectionActionBar;
            if (work != DeferredChromeWork.None)
                ScheduleChromeRefresh(work);
        };

        controller.ScaleChanged += (_, e) =>
        {
            currentScale = e.Scale;
            RefreshActionState();
            UpdateStatus($"Scale: {e.Scale * 100:F0}%");
            ScheduleChromeRefresh(DeferredChromeWork.Summary);
        };
        controller.LongPress += (_, e) =>
        {
            (bool hasSelection, TextRange range) = controller.GetSelection();
            if (hasSelection)
                CaptureSelectionActionAnchor(range, e.CursorPosition, e.ScreenPoint);
            else
                CaptureSelectionActionAnchor(null, e.CursorPosition, e.ScreenPoint);
            UpdateStatus($"Long press: {e.ScreenPoint.X:F0},{e.ScreenPoint.Y:F0}");
            Dispatcher.UIThread.Post(
                () => RefreshSelectionActionBarWithStabilization(forceRetry: IsImeVisibleInEditorHost()),
                DispatcherPriority.Background);
        };
        controller.DoubleTap += (_, e) =>
        {
            if (e.HasSelection && e.Selection is TextRange selection)
                CaptureSelectionActionAnchor(selection, e.CursorPosition, e.ScreenPoint);
            else
                CaptureSelectionActionAnchor(null, e.CursorPosition, e.ScreenPoint);
            UpdateStatus($"Double tap: {e.ScreenPoint.X:F0},{e.ScreenPoint.Y:F0}");
            Dispatcher.UIThread.Post(
                () => RefreshSelectionActionBarWithStabilization(forceRetry: IsImeVisibleInEditorHost()),
                DispatcherPriority.Background);
        };
        controller.ContextMenu += (_, _) => UpdateStatus("Context menu requested");
        controller.GutterIconClick += (_, e) => UpdateStatus($"Gutter icon at line {e.Line}");
        controller.FoldToggle += (_, e) =>
        {
            foldCollapsed = !controller.IsLineVisible(e.Line + 1);
            UpdateStatus($"Fold toggled at line {e.Line} ({(e.IsGutter ? "gutter" : "placeholder")})");
            UpdateFeatureStrip();
        };
        controller.InlayHintClick += (_, e) =>
        {
            if (e.Type == InlayType.Color)
                UpdateStatus($"Color inlay clicked: 0x{e.IntValue:X8}");
            else if (e.Type == InlayType.Icon)
                UpdateStatus($"Icon inlay clicked: {e.IntValue}");
            else
                UpdateStatus($"Inlay clicked at {e.Line}:{e.Column}");
        };
        controller.CompletionItemsUpdated += items =>
        {
            Dispatcher.UIThread.Post(() => ShowCompletionItems(items));
        };
        controller.CompletionDismissed += () => Dispatcher.UIThread.Post(() =>
        {
            HideCompletionPopup();
            MaybeShowInlineSuggestion(controller.GetCursorPosition());
            UpdateFeatureStrip();
        });
        controller.InlineSuggestionAccepted += suggestion =>
        {
            CancelPendingInlineSuggestion();
            UpdateStatus($"Ghost accepted at {suggestion.Line}:{suggestion.Column}");
            ScheduleChromeRefresh(DeferredChromeWork.SelectionActionBar);
        };
        controller.InlineSuggestionDismissed += suggestion =>
        {
            CancelPendingInlineSuggestion();
            UpdateStatus($"Ghost dismissed at {suggestion.Line}:{suggestion.Column}");
            ScheduleChromeRefresh(DeferredChromeWork.SelectionActionBar);
        };

        AddHandler(InputElement.PointerPressedEvent, OnMainViewPointerPressed, AvaInteractivity.RoutingStrategies.Tunnel);
    }

    private void ScheduleEditorInitialization()
    {
        if (editorInitializationScheduled)
            return;

        editorInitializationScheduled = true;
        Dispatcher.UIThread.Post(() =>
        {
            editorInitializationScheduled = false;
            EnsureEditorCreated();
        }, DispatcherPriority.Background);
    }

    private void EnsureEditorCreated()
    {
        if (editor != null)
            return;

        SweetEditorControl createdEditor = new(controller)
        {
            HorizontalAlignment = HorizontalAlignment.Stretch,
            VerticalAlignment = VerticalAlignment.Stretch,
            Focusable = true,
        };
        if (DemoPlatformServices.Current?.IsAndroid == true)
            RenderOptions.SetTextRenderingMode(createdEditor, TextRenderingMode.Alias);

        createdEditor.AddHandler(InputElement.KeyDownEvent, OnEditorKeyDown, AvaInteractivity.RoutingStrategies.Tunnel);
        editor = createdEditor;
        editorHost.Children.Insert(0, createdEditor);
        InitializeDemo();
    }

    private void InitializeDemo()
    {
        if (demoInitialized)
            return;

        demoInitialized = true;
        controller.SetEditorIconProvider(iconProvider);
        controller.AddDecorationProvider(decorationProvider);
        controller.AddCompletionProvider(completionProvider);
        controller.AddNewLineActionProvider(newLineActionProvider);
        controller.SetInlineSuggestionListener(new DemoInlineSuggestionListener(UpdateStatus));
        controller.SetSelectionMenuHostManaged(true);
        controller.SetKeyMap(CreateEditorKeyMap());

        EditorSettings? settings = controller.GetSettings();
        if (settings != null)
        {
            bool isAndroid = DemoPlatformServices.Current?.IsAndroid == true;
            settings.SetTypeface("monospace");
            settings.SetEditorTextSize(isAndroid ? 14.0f : 13.25f);
            currentScale = GetDefaultScale();
            settings.SetScale(currentScale);
            settings.SetContentStartPadding(2);
            settings.SetFoldArrowMode(FoldArrowMode.AUTO);
            settings.SetCurrentLineRenderMode(CurrentLineRenderMode.BORDER);
            settings.SetGutterSticky(!isAndroid);
            settings.SetMaxGutterIcons(1);
            settings.SetWrapMode(wrapMode);
            settings.SetAutoIndentMode(AutoIndentMode.KEEP_INDENT);
            settings.SetBackspaceUnindent(true);
            settings.SetDecorationScrollRefreshMinIntervalMs(isAndroid ? 80 : 64);
            settings.SetDecorationOverscanViewportMultiplier(isAndroid ? 0.20f : 0.12f);
        }

        ApplyTheme();
        sampleLoader.LoadInitialSamples(typeof(MainView).Assembly);
        SyncSamplePickerSelection();
        RefreshActionState();
        UpdateFeatureStrip();
        if (sampleLoader.SampleFiles.Count > 0)
            UpdateStatus($"Loaded {sampleLoader.SampleFiles.Count} demo samples");
        ScheduleChromeRefresh(DeferredChromeWork.Summary);
    }

    private EditorKeyMap CreateEditorKeyMap()
    {
        EditorKeyMap keyMap = new(useVsCodeKeyMap ? KeyMap.Vscode().Bindings : KeyMap.DefaultKeyMap().Bindings);
        keyMap.RegisterCommand(
            new KeyBinding(new KeyChord(KeyModifier.CTRL, KeyCode.K), new KeyChord(KeyModifier.CTRL, KeyCode.D), 0),
            _ =>
            {
                controller.TriggerCompletion();
                UpdateStatus("Host keymap: completion triggered");
                return true;
            });
        return keyMap;
    }

    private void ApplyTheme()
    {
        ThemeVariant variant = darkTheme ? ThemeVariant.Dark : ThemeVariant.Light;
        Application.Current!.RequestedThemeVariant = variant;

        uint background = darkTheme ? 0xFF1B1E24 : 0xFFFAFBFD;
        uint surface = background;
        uint surfaceMuted = darkTheme ? 0xFF242A33 : 0xFFF1F4F8;
        uint border = darkTheme ? 0xFF313844 : 0xFFD7DEE8;
        uint foreground = darkTheme ? 0xFFD7DEE9 : 0xFF1F2937;
        uint secondary = darkTheme ? 0xFF7A8494 : 0xFF8A94A6;
        uint accent = darkTheme ? 0xFF4C9DFF : 0xFF0F7AD9;

        toolbarContainer.Background = CreateBrush(surface);
        toolbarContainer.BorderBrush = CreateBrush(border);
        Background = CreateBrush(background);
        statusBar.Background = CreateBrush(surface);
        statusBar.BorderBrush = CreateBrush(border);
        statusText.Foreground = CreateBrush(foreground);
        summaryText.Foreground = CreateBrush(secondary);
        samplePickerPopup.Background = CreateBrush(darkTheme ? 0xFF1B1E24u : 0xFFFFFFFFu);
        samplePickerPopup.BorderBrush = CreateBrush(darkTheme ? 0xFF313844u : 0xFFD7DEE8u);
        completionPopup.Background = CreateBrush(darkTheme ? 0xFF1B1E24u : 0xFFFFFFFFu);
        completionPopup.BorderBrush = CreateBrush(darkTheme ? 0xFF313844u : 0xFFD7DEE8u);
        selectionActionBar.Background = CreateBrush(darkTheme ? 0xFF1B1E24u : 0xFFFFFFFFu);
        selectionActionBar.BorderBrush = CreateBrush(darkTheme ? 0xFF313844u : 0xFFD7DEE8u);

        notificationPanel.DarkTheme = darkTheme;

        toolbarController.SamplePickerChrome.Background = CreateBrush(surfaceMuted);
        toolbarController.SamplePickerChrome.BorderBrush = CreateBrush(border);
        toolbarController.SamplePickerChrome.BorderThickness = new Thickness(1);
        toolbarController.SamplePickerLabel.Foreground = CreateBrush(foreground);
        toolbarController.SamplePickerChevron.Stroke = CreateBrush(foreground);
        toolbarController.SetTheme(darkTheme);
        toolbarController.SetWrapMode(wrapMode);
        ApplyToolbarChrome(foreground, surfaceMuted, border, accent);

        controller.ApplyTheme(CreateEditorTheme(darkTheme));
        if (samplePickerPopup.IsVisible)
        {
            RebuildSamplePickerItems();
            ScheduleChromeRefresh(DeferredChromeWork.SamplePickerPopup);
        }
        if (completionPopup.IsVisible)
        {
            RenderCompletionItems();
        }
        if (selectionActionBar.IsVisible)
        {
            RebuildSelectionActionBarItems(BuildSelectionActionBarItems());
        }
    }

    private static EditorTheme CreateEditorTheme(bool dark)
    {
        uint colorToken = dark ? 0xFF9CDCFEu : 0xFF0F766Eu;
        return (dark ? EditorTheme.Dark() : EditorTheme.Light())
            .DefineTextStyle((uint)DemoDecorationProvider.StyleColor, new TextStyle(unchecked((int)colorToken), 0));
    }

    private void PrepareForSampleLoad(DemoSampleFile sample)
    {
        activeSample = sample;
        currentLanguageId = sample.LanguageId;
        foldCollapsed = false;
        allowPassiveInlineSuggestion = false;
        CancelPendingInlineSuggestion();
        controller.DismissCompletion();
        controller.DismissInlineSuggestion();
        HideSamplePickerPopup();
        HideCompletionPopup();
        HideSelectionActionBar();
        controller.GetSettings()?.SetGutterSticky(DemoPlatformServices.Current?.IsAndroid != true);

        loadingIndicator.IsLoading = true;
        loadingIndicator.LoadingText = $"Loading {sample.FileName}...";

        UpdateFeatureStrip();
    }

    private void ApplyLanguageConfiguration(string languageId)
    {
        currentLanguageId = languageId;
        controller.SetLanguageConfiguration(languageId.Equals("lua", StringComparison.OrdinalIgnoreCase)
            ? new LanguageConfiguration(
                "lua",
                brackets: new List<BracketPair> { new("(", ")"), new("{", "}"), new("[", "]") },
                autoClosingPairs: new List<BracketPair>
                {
                    new("(", ")"),
                    new("{", "}"),
                    new("[", "]"),
                    new("\"", "\""),
                    new("'", "'")
                },
                lineComment: "--",
                tabSize: 4,
                insertSpaces: true)
            : new LanguageConfiguration(
                languageId,
                brackets: new List<BracketPair> { new("(", ")"), new("{", "}"), new("[", "]") },
                autoClosingPairs: new List<BracketPair>
                {
                    new("(", ")"),
                    new("{", "}"),
                    new("[", "]"),
                    new("\"", "\""),
                    new("'", "'")
                },
                lineComment: "//",
                blockComment: new BlockComment("/*", "*/"),
                tabSize: 4,
                insertSpaces: true));

        UpdateFeatureStrip();
    }

    private void ToggleTheme()
    {
        darkTheme = !darkTheme;
        HideCompletionPopup();
        ApplyTheme();
        controller.RequestDecorationRefresh();
        UpdateFeatureStrip();
        UpdateStatus(darkTheme ? "Theme: dark" : "Theme: light");
    }

    private void CycleWrapMode()
    {
        WrapMode[] modes = Enum.GetValues<WrapMode>();
        int index = Array.IndexOf(modes, wrapMode);
        wrapMode = modes[(index + 1) % modes.Length];
        controller.GetSettings()?.SetWrapMode(wrapMode);
        toolbarController.SetWrapMode(wrapMode);
        UpdateFeatureStrip();
        UpdateStatus($"Wrap: {wrapMode}");
    }

    private void StepScale(int delta)
    {
        EditorSettings? settings = controller.GetSettings();
        if (settings == null)
            return;

        int nearestIndex = 0;
        float bestDistance = float.MaxValue;
        for (int i = 0; i < ScalePresets.Length; i++)
        {
            float distance = Math.Abs(ScalePresets[i] - currentScale);
            if (distance < bestDistance)
            {
                bestDistance = distance;
                nearestIndex = i;
            }
        }

        int nextIndex = Math.Clamp(nearestIndex + delta, 0, ScalePresets.Length - 1);
        float nextScale = ScalePresets[nextIndex];
        currentScale = nextScale;
        settings.SetScale(nextScale);
        RefreshActionState();
        UpdateStatus($"Scale: {nextScale * 100:F0}%");
        ScheduleChromeRefresh(DeferredChromeWork.Summary);
    }

    private float GetDefaultScale() => DemoPlatformServices.Current?.IsAndroid == true ? 0.90f : 0.96f;

    private void TogglePerfOverlay()
    {
        perfOverlayEnabled = !perfOverlayEnabled;
        controller.SetPerfOverlayEnabled(perfOverlayEnabled);
        UpdateFeatureStrip();
        UpdateStatus(perfOverlayEnabled ? "Perf overlay: ON" : "Perf overlay: OFF");
    }

    private void ToggleKeyMap()
    {
        useVsCodeKeyMap = !useVsCodeKeyMap;
        controller.SetKeyMap(CreateEditorKeyMap());
        UpdateFeatureStrip();
