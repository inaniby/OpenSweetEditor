    private void ScheduleChromeRefresh(DeferredChromeWork work)
    {
        ScheduleChromeRefresh(work, DispatcherPriority.Background);
    }

    private void ScheduleChromeRefresh(DeferredChromeWork work, DispatcherPriority priority)
    {
        pendingChromeWork |= work;
        if (chromeRefreshScheduled)
            return;

        chromeRefreshScheduled = true;
        Dispatcher.UIThread.Post(ApplyScheduledChromeRefresh, priority);
    }

    private void ApplyScheduledChromeRefresh()
    {
        chromeRefreshScheduled = false;
        DeferredChromeWork work = pendingChromeWork;
        pendingChromeWork = DeferredChromeWork.None;

        if ((work & DeferredChromeWork.CursorVisibility) != 0)
            EnsureCursorVisibleForAndroid();

        if ((work & DeferredChromeWork.SamplePickerPopup) != 0 && samplePickerPopup.IsVisible)
            RepositionSamplePickerPopup();

        if ((work & DeferredChromeWork.CompletionPopup) != 0 && completionPopup.IsVisible)
            RepositionCompletionPopup();

        if ((work & DeferredChromeWork.SelectionActionBar) != 0)
            UpdateSelectionActionBar();

        if ((work & DeferredChromeWork.Summary) != 0)
            UpdateSummary();
    }

