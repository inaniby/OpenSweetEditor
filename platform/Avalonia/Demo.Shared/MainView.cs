    private void RepositionCompletionPopup()
    {
        if (!completionPopup.IsVisible)
            return;

        double hostWidth = editorHost.Bounds.Width;
        double hostHeight = editorHost.Bounds.Height;
        if (hostWidth <= 0 || hostHeight <= 0)
            return;

        CursorRect rect = controller.GetCursorRect();
        double margin = 6;
        double width = Math.Min(CompletionPopupWidth, Math.Max(CompletionPopupMinWidth, hostWidth - margin * 2));
        if (width > hostWidth - margin * 2)
            width = Math.Max(120, hostWidth - margin * 2);

        double cursorLeft = rect.X;
        double cursorTop = rect.Y;
        double cursorBottom = cursorTop + rect.Height;
        double safeBottom = hostHeight - margin;
        if (DemoPlatformServices.Current?.TryGetImeTopInEditorHostDip(this, editorHost, out double imeTopInHostDip) == true)
            safeBottom = Math.Min(safeBottom, imeTopInHostDip - margin);

        double popupHeight = Math.Min(
            CompletionRowHeight * Math.Min(Math.Max(currentCompletionItems.Count, 1), CompletionMaxVisibleItems) + 12,
            Math.Max(CompletionRowHeight + 12, safeBottom - margin));

        double x = Math.Clamp(cursorLeft, margin, Math.Max(margin, hostWidth - width - margin));
        double belowY = cursorBottom + 4;
        double aboveY = cursorTop - popupHeight - 4;
        double y;
        if (belowY + popupHeight <= safeBottom)
            y = belowY;
        else if (aboveY >= margin)
            y = aboveY;
        else
            y = Math.Clamp(belowY, margin, Math.Max(margin, safeBottom - popupHeight));

        completionPopup.Width = width;
        completionPopup.MaxHeight = popupHeight;
        completionPopup.Margin = new Thickness(x, y, 0, 0);
    }

    private void ApplyCompletionItem(CompletionItem item)
    {
        string text = item.InsertText ?? item.Label;
        if (item.TextEditValue != null)
        {
            controller.DeleteText(item.TextEditValue.Range);
            text = item.TextEditValue.NewText;
        }
        else if (TryGetSafeCompletionReplacementRange(out TextRange range))
        {
            controller.DeleteText(range);
        }

        if (item.InsertTextFormat == CompletionItem.INSERT_TEXT_FORMAT_SNIPPET)
            controller.InsertSnippet(text);
        else
            controller.InsertText(text);

        RefreshActionState();
        controller.DismissCompletion();
    }

    private void OnEditorKeyDown(object? sender, KeyEventArgs e)
    {
        if (samplePickerPopup.IsVisible && e.Key == Key.Escape)
        {
            HideSamplePickerPopup();
            e.Handled = true;
            return;
        }

        if (completionPopup.IsVisible && currentCompletionItems.Count > 0)
        {
            if (e.Key == Key.Down)
            {
                MoveCompletionSelection(1);
                e.Handled = true;
                return;
            }

            if (e.Key == Key.Up)
            {
                MoveCompletionSelection(-1);
                e.Handled = true;
                return;
            }

            if (e.Key == Key.Enter && TryGetSelectedCompletionItem(out CompletionItem? item))
            {
                ApplyCompletionItem(item!);
                UpdateStatus($"Completion applied: {item!.Label}");
                HideCompletionPopup();
                e.Handled = true;
                return;
            }
        }

        if (completionPopup.IsVisible && e.Key == Key.Escape)
        {
            HideCompletionPopup();
            controller.DismissCompletion();
            e.Handled = true;
            return;
        }

        if (e.Key == Key.F1)
        {
            TriggerCompletion();
            e.Handled = true;
            return;
        }

        if (e.Key == Key.F2)
        {
            ShowInlineSuggestionAtCursor();
            e.Handled = true;
            return;
        }

        if (e.Key == Key.F3)
        {
            InsertSnippetAtCursor();
            e.Handled = true;
        }
    }

    private void EnsureCursorVisibleForAndroid()
    {
        IDemoPlatformServices? platform = DemoPlatformServices.Current;
        if (platform?.IsAndroid != true)
            return;

        if (!platform.TryGetImeTopInEditorHostDip(this, editorHost, out double imeTopInHostDip))
            return;

        CursorRect rect = controller.GetCursorRect();
        ScrollMetrics scroll = controller.GetScrollMetrics();
        double safeTop = 8;
        double safeBottom = imeTopInHostDip - 12;
        double cursorTopInViewport = rect.Y;
        double cursorBottomInViewport = rect.Y + Math.Max(1, rect.Height);
        if (cursorBottomInViewport > safeBottom)
        {
            controller.SetScroll(scroll.ScrollX, scroll.ScrollY + (float)(cursorBottomInViewport - safeBottom));
        }
        else if (cursorTopInViewport < safeTop)
        {
            controller.SetScroll(scroll.ScrollX, Math.Max(0, scroll.ScrollY - (float)(safeTop - cursorTopInViewport)));
        }
    }

