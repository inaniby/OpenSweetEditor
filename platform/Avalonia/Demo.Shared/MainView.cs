    private void MaybeShowInlineSuggestion(TextPosition cursor)
    {
        CancelPendingInlineSuggestion();
        if (!allowPassiveInlineSuggestion || !inlineSuggestionAutoEnabled || controller.IsInlineSuggestionShowing() || completionPopup.IsVisible)
            return;

        (bool hasSelection, _) = controller.GetSelection();
        if (hasSelection)
            return;

        Document? doc = controller.GetDocument();
        if (doc == null)
            return;

        string lineText = doc.GetLineText(cursor.Line) ?? string.Empty;
        if (lineText.Trim().Length == 0 || cursor.Column != lineText.Length)
            return;

        int token = ++pendingInlineSuggestionToken;
        DispatcherTimer.RunOnce(() =>
        {
            if (token != pendingInlineSuggestionToken || controller.IsInlineSuggestionShowing() || completionPopup.IsVisible)
                return;

            controller.ShowInlineSuggestion(new InlineSuggestion(cursor.Line, cursor.Column, InlineSuggestionText));
            UpdateSelectionActionBar();
        }, TimeSpan.FromMilliseconds(DemoPlatformServices.Current?.IsAndroid == true ? 1000 : 700));
    }

