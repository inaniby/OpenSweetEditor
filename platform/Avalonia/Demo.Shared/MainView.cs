    private static void ApplyActionChrome(Border chrome, uint foreground, uint surfaceMuted, uint border, uint background, uint textColor)
    {
        chrome.Background = CreateBrush(background);
        chrome.BorderBrush = CreateBrush(border);
        chrome.BorderThickness = new Thickness(1);

        if (chrome.Child is AvaPath path)
        {
            path.Fill = CreateBrush(textColor);
            return;
        }

        if (chrome.Child is TextBlock label)
        {
            label.Foreground = CreateBrush(textColor);
            return;
        }

        if (chrome.Child is Grid grid)
        {
            foreach (Control child in grid.Children.OfType<Control>())
            {
                if (child is AvaPath iconPath)
                {
                    iconPath.Fill = CreateBrush(textColor);
                }
                else if (child is Border badge && badge.Child is TextBlock badgeText)
                {
                    badge.Background = CreateBrush(textColor);
                    badgeText.Foreground = CreateBrush(surfaceMuted);
                }
            }
        }
    }

