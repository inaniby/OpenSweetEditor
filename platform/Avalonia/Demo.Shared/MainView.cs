        bool inlineShowing = controller.IsInlineSuggestionShowing();
        bool hasTransientSelectionAnchor = !hasSelection && !inlineShowing && TryGetSelectionActionAnchor(out SelectionActionAnchorSnapshot snapshot) && snapshot.HasSelection;
        if (!hasSelection && !inlineShowing && !hasTransientSelectionAnchor)
        {
            HideSelectionActionBar();
