                ShowInlineSuggestionAtCursor();
                break;
            case DemoSelectionMenuItemProvider.ActionToggleInlineSuggestionAuto:
                inlineSuggestionAutoEnabled = !inlineSuggestionAutoEnabled;
                UpdateFeatureStrip();
                UpdateStatus(inlineSuggestionAutoEnabled ? "Auto ghost: ON" : "Auto ghost: OFF");
                break;
