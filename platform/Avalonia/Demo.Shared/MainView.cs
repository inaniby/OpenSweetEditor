    private void CaptureSelectionActionAnchor(TextRange? range, TextPosition? cursorOverride, PointF? gesturePoint)
    {
        Point? anchorPoint = ToAnchorPoint(gesturePoint);
        if (!anchorPoint.HasValue &&
            range is TextRange carriedSelection &&
            TryGetCarriedSelectionAnchorPoint(carriedSelection, out Point preservedAnchorPoint))
        {
            anchorPoint = preservedAnchorPoint;
        }

        if (range is TextRange &&
            !anchorPoint.HasValue &&
            ShouldPreferGestureOnlySelectionAnchor() &&
            TryCreateCursorActionAnchorSnapshot(cursorOverride, null, out SelectionActionAnchorSnapshot deferredCursorSnapshot))
        {
            recentSelectionActionAnchor = deferredCursorSnapshot;
            recentSelectionActionAnchorTickMs = Environment.TickCount64;
            return;
        }

        if (range is TextRange selection && TryCreateSelectionActionAnchorSnapshot(selection, anchorPoint, out SelectionActionAnchorSnapshot selectionSnapshot))
        {
            recentSelectionActionAnchor = selectionSnapshot;
            recentSelectionActionAnchorTickMs = Environment.TickCount64;
            return;
        }

        if (TryCreateCursorActionAnchorSnapshot(cursorOverride, anchorPoint, out SelectionActionAnchorSnapshot cursorSnapshot))
        {
            recentSelectionActionAnchor = cursorSnapshot;
            recentSelectionActionAnchorTickMs = Environment.TickCount64;
            return;
        }

        if (range is not TextRange)
        {
            if (!IsImeVisibleInEditorHost())
                recentSelectionActionAnchor = null;
        }
    }

    private bool TryGetSelectionActionAnchor(out SelectionActionAnchorSnapshot anchor)
    {
        (bool hasSelection, TextRange selectionRange) = controller.GetSelection();
        Point? cachedSelectionAnchorPoint = TryResolveSelectionAnchorPoint(selectionRange);
        if (hasSelection && TryCreateSelectionActionAnchorSnapshot(selectionRange, cachedSelectionAnchorPoint, out SelectionActionAnchorSnapshot liveSelectionAnchor))
        {
            anchor = liveSelectionAnchor;
            recentSelectionActionAnchor = anchor;
            recentSelectionActionAnchorTickMs = Environment.TickCount64;
            return true;
        }

        Point? cachedCursorAnchorPoint = recentSelectionActionAnchor is SelectionActionAnchorSnapshot cachedCursor &&
                                         cachedCursor.HasAnchorPoint
            ? cachedCursor.AnchorPoint
            : null;
        if (controller.IsInlineSuggestionShowing() && TryCreateCursorActionAnchorSnapshot(null, cachedCursorAnchorPoint, out SelectionActionAnchorSnapshot liveCursorAnchor))
        {
            anchor = liveCursorAnchor;
            recentSelectionActionAnchor = anchor;
            recentSelectionActionAnchorTickMs = Environment.TickCount64;
            return true;
        }

        if (!IsImeVisibleInEditorHost() || recentSelectionActionAnchor is not SelectionActionAnchorSnapshot cached)
        {
            anchor = default;
            return false;
        }

        if ((Environment.TickCount64 - recentSelectionActionAnchorTickMs) > SelectionActionAnchorRetentionMs)
        {
            recentSelectionActionAnchor = null;
            anchor = default;
            return false;
        }

        anchor = cached;
        return true;
    }

    private bool IsRecentSelectionActionAnchorAvailable()
    {
        return recentSelectionActionAnchor is SelectionActionAnchorSnapshot anchor &&
               anchor.HasSelection &&
               (Environment.TickCount64 - recentSelectionActionAnchorTickMs) <= SelectionActionAnchorRetentionMs;
    }

    private bool TryCreateSelectionActionAnchorSnapshot(TextRange range, Point? anchorPoint, out SelectionActionAnchorSnapshot snapshot)
    {
        snapshot = default;
        Document? document = controller.GetDocument();
        if (document == null || !TrySanitizeSelectionRange(range, document, out TextRange sanitizedRange))
            return false;

        CursorRect startRect = default;
        CursorRect endRect = default;
        if (!(anchorPoint.HasValue && ShouldPreferGestureOnlySelectionAnchor()))
        {
            startRect = controller.GetPositionRect(sanitizedRange.Start.Line, sanitizedRange.Start.Column);
            endRect = controller.GetPositionRect(sanitizedRange.End.Line, sanitizedRange.End.Column);
        }

        snapshot = new SelectionActionAnchorSnapshot(
            sanitizedRange,
            startRect,
            endRect,
            controller.GetCursorRect(),
            anchorPoint ?? default,
            anchorPoint.HasValue,
            true);
        return true;
    }

    private bool TryCreateCursorActionAnchorSnapshot(TextPosition? cursorOverride, Point? anchorPoint, out SelectionActionAnchorSnapshot snapshot)
    {
        snapshot = default;
        CursorRect cursorRect = controller.GetCursorRect();
        if (cursorOverride.HasValue && controller.GetDocument() is Document document && TryClampPosition(cursorOverride.Value, document, Math.Max(0, document.GetLineCount()), out TextPosition clampedCursor))
        {
            cursorRect = controller.GetPositionRect(clampedCursor.Line, clampedCursor.Column);
        }

        if (cursorRect.Height <= 0)
            return false;

        snapshot = new SelectionActionAnchorSnapshot(
            default,
            default,
            default,
            cursorRect,
            anchorPoint ?? default,
            anchorPoint.HasValue,
            false);
        return true;
    }

    private static Point? ToAnchorPoint(PointF? gesturePoint)
    {
        if (gesturePoint is not PointF point)
            return null;

        return new Point(point.X, point.Y);
    }

    private bool TryGetCarriedSelectionAnchorPoint(TextRange selection, out Point anchorPoint)
    {
        anchorPoint = default;
        Point? resolved = TryResolveSelectionAnchorPoint(selection);
        if (!resolved.HasValue)
            return false;

        anchorPoint = resolved.Value;
        return true;
    }

    private Point? TryResolveSelectionAnchorPoint(TextRange selection)
    {
        if (recentSelectionActionAnchor is not SelectionActionAnchorSnapshot cached ||
            !cached.HasSelection ||
            !cached.HasAnchorPoint)
        {
            return null;
        }

        if ((Environment.TickCount64 - recentSelectionActionAnchorTickMs) > SelectionActionAnchorRetentionMs)
            return null;

        Document? document = controller.GetDocument();
        if (document == null ||
            !TrySanitizeSelectionRange(selection, document, out TextRange sanitizedSelection) ||
            !TrySanitizeSelectionRange(cached.Range, document, out TextRange sanitizedCached))
        {
            return null;
        }

        return RangesOverlapOrTouch(sanitizedCached, sanitizedSelection)
            ? cached.AnchorPoint
            : null;
    }

    private bool ShouldPreferGestureOnlySelectionAnchor()
    {
        if (DemoPlatformServices.Current?.IsAndroid != true)
            return false;

        if (IsImeVisibleInEditorHost())
            return true;

        Document? document = controller.GetDocument();
        return Math.Max(0, document?.GetLineCount() ?? 0) >= 12000;
    }

    private bool ShouldDeferLargeDocumentUiRefresh()
    {
        if (DemoPlatformServices.Current?.IsAndroid != true)
            return false;

        Document? document = controller.GetDocument();
        return Math.Max(0, document?.GetLineCount() ?? 0) >= 12000;
    }

    private static bool TrySanitizeSelectionRange(TextRange range, Document document, out TextRange sanitized)
    {
        sanitized = default;
        int lineCount = Math.Max(0, document.GetLineCount());
        if (lineCount <= 0)
            return false;

        TextRange normalized = NormalizeRange(range);
        if (!TryClampPosition(normalized.Start, document, lineCount, out TextPosition start) ||
            !TryClampPosition(normalized.End, document, lineCount, out TextPosition end))
        {
            return false;
        }

        if (ComparePositions(start, end) >= 0)
            return false;

        sanitized = new TextRange { Start = start, End = end };
        return true;
    }

    private static bool RangesOverlapOrTouch(TextRange left, TextRange right)
    {
        TextRange normalizedLeft = NormalizeRange(left);
        TextRange normalizedRight = NormalizeRange(right);
        return ComparePositions(normalizedLeft.End, normalizedRight.Start) >= 0 &&
               ComparePositions(normalizedRight.End, normalizedLeft.Start) >= 0;
    }

    private static bool TryClampPosition(TextPosition position, Document document, int lineCount, out TextPosition clamped)
    {
        clamped = default;
        if (lineCount <= 0)
            return false;

        int line = Math.Clamp(position.Line, 0, lineCount - 1);
        string lineText = document.GetLineText(line) ?? string.Empty;
        int column = Math.Clamp(position.Column, 0, lineText.Length);
        clamped = new TextPosition { Line = line, Column = column };
        return true;
    }

    private void LogSelectionActionBarState(string reason)
    {
        if (DemoPlatformServices.Current?.IsAndroid != true)
            return;

        bool imeVisible = IsImeVisibleInEditorHost();
        bool hasSelection = controller.GetSelection().hasSelection;
        bool inlineShowing = controller.IsInlineSuggestionShowing();
        bool hasAnchor = !hasSelection && !inlineShowing && IsRecentSelectionActionAnchorAvailable();
        if (!imeVisible && !selectionActionBar.IsVisible && !hasAnchor)
            return;

        Console.Error.WriteLine(
            $"SelectionActionBar[{reason}]: visible={selectionActionBar.IsVisible} actual={hasSelection} inline={inlineShowing} anchor={hasAnchor} ime={imeVisible}");
    }

