    private IReadOnlyList<SelectionMenuItem> BuildSelectionActionBarItems()
    {
        bool hasSelection = controller.GetSelection().hasSelection;
        List<SelectionMenuItem> merged = new()
        {
            new(SelectionMenuItem.ACTION_CUT, "Cut", hasSelection),
            new(SelectionMenuItem.ACTION_COPY, "Copy", hasSelection),
            new(SelectionMenuItem.ACTION_PASTE, "Paste"),
            new(SelectionMenuItem.ACTION_SELECT_ALL, "Select All"),
        };

        if (editor == null)
            return merged;

        foreach (SelectionMenuItem item in selectionMenuProvider.ProvideMenuItems(editor))
        {
            if (string.IsNullOrWhiteSpace(item.Id))
            {
                continue;
            }

            int existingIndex = merged.FindIndex(existing => string.Equals(existing.Id, item.Id, StringComparison.Ordinal));
            if (existingIndex >= 0)
            {
                merged[existingIndex] = item;
            }
            else
            {
                merged.Add(item);
            }
        }

        if (hasSelection && merged.All(item => !string.Equals(item.Id, DemoSelectionMenuItemProvider.ActionDeleteSelection, StringComparison.Ordinal)))
        {
            merged.Add(new SelectionMenuItem(DemoSelectionMenuItemProvider.ActionDeleteSelection, "Delete", true));
        }

        return merged;
    }

