                FoldAll();
                break;
            case DemoSelectionMenuItemProvider.ActionUnfoldAll:
                UnfoldAll();
                break;
            case DemoSelectionMenuItemProvider.ActionLoadLargeSample:
                LoadLargeSample();
                break;
            case DemoSelectionMenuItemProvider.ActionTogglePerfOverlay:
                TogglePerfOverlay();
                break;
            case DemoSelectionMenuItemProvider.ActionToggleKeyMap:
                ToggleKeyMap();
                break;
        }

        Dispatcher.UIThread.Post(() => ScheduleChromeRefresh(DeferredChromeWork.SelectionActionBar), DispatcherPriority.Background);
    }

